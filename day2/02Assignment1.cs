// 1. Write a C# Programs to implement the following requirements (using while): 

//  a.  Generate 5 multiples :5  10 15  20  .......   50 

using System;

class Assignment
{

    static void Main()
    {

        int i = 5;

        while (i <= 50)

        {

            Console.WriteLine(i);

            i += 5;

        }

    }

} 

 

 

